# Ao questionar algo em um programa, só existem duas respostas possíveis:
# sim (True) ou não (False).
# Existem vários operadores para questionar. Dentre eles o ==
# Ele questiona se um valor é igual ao outro.
print(1 == 1) # True
print(1 == 10) # False
print(type(10 == 10), type(1 == 1))
print(type(True), type(False))