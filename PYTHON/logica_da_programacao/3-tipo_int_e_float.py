# int --> Número inteiro positivou ou negativo escrito sem outros caracteres.
print(2000)
print(-11)

# float --> Número com ponto flutuante (casa decimal).
print(1.10, 1500.53)
print(-33.333, 0.5)

# A função type é utilizada para mostrar o tipo de dado.
print(type('David')) # tipo str
print(type(-11), type(55)) # tipo int
print(type(0.5), type(0.0), type(-33.68)) #tipo float