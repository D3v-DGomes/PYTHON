# Strings ou str nada mais são que textos dentro de aspas.

# Com aspas simples
print('David Gomes')

# Com aspas duplas
print("David Gomes")

# Escape
print("David \"Gomes\"") # Serve para exibir as aspas que estão contidas na string sem que aja erro pelo interpretador.

# r
print(r"David \"Gomes\"") # Utilizado para exibir as aspas que estão contidas na string. Útil para expressões regulares.

# Jeito mais prático para uso cotidiano
print('David "Gomes"')
print("'David' Gomes")